export default function Server({ slug }) {
  const Components = components[slug]
  return (
    <Layout>
      {Components.map((Component, index) => {
        return (
          <div
            className="relative min-w-full p-1 border-b border-gray-400 my-5 min-h-[400px]"
            key={index}
          >
            <h3 className="text-sm font-medium p-2 tracking-wide text-gray-500 uppercase">
              {Component.name}
            </h3>
            <Component.component />
          </div>
        )
      })}
    </Layout>
  )
}

export async function getStaticPaths() {
  return {
    paths: slugs.map((slugKey) => ({
      params: {
        element: slugKey.slug,
      },
    })),
    fallback: false,
  }
}

export async function getStaticProps({ params }) {
  const slug = params.element
  return {
    props: {
      slug,
    },
  }
}
