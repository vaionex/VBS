export async function getStaticPaths() {
  return {
    paths: slugs.map((slugKey) => ({
      params: {
        element: slugKey.slug,
      },
    })),
    fallback: false,
  }
}

export async function getStaticProps({ params }) {
  const slug = params.element
  return {
    props: {
      slug,
    },
  }
}
